package PATTERNS;

public public class pattern2 
{
    public static void main(String args[])
    {
        int i,j,n=8;
        for(i=0;i<n;i++)
        {
            for(j=n;j>i;j--)
            {
                System.out.print("# ");
            }
            System.out.print("\n");
        }
    }
}
